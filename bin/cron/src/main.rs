use reqwest::Client;
use serde::Deserialize;
use std::collections::HashMap;
use cluebot_email::{EmailConfig, EmailChannel};
use cluebot_engine::Channel;
use std::env;

const BASE_URL: &str = "https://www.okx.com";

#[derive(Debug, Deserialize)]
struct ApiResponse<T> {
    code: String,
    msg: Option<String>,
    data: Vec<T>,
}

#[derive(Debug, Deserialize)]
struct Ticker {
    #[serde(rename = "instId")]
    inst_id: String,
}

#[derive(Debug, Deserialize)]
struct Candle(Vec<String>);

async fn get_tickers(client: &Client, inst_type: &str) -> Vec<Ticker> {
    let url = format!("{}/api/v5/market/tickers?instType={}", BASE_URL, inst_type);
    let resp = client.get(&url).send().await.unwrap().json::<ApiResponse<Ticker>>().await.unwrap();
    if resp.code != "0" {
        panic!("API error: {:?}", resp.msg);
    }
    resp.data
}

async fn get_candles(client: &Client, inst_id: &str) -> Vec<Candle> {
    let url = format!(
        "{}/api/v5/market/candles?instId={}&bar=1H&limit=9",
        BASE_URL, inst_id
    );
    let resp = client.get(&url).send().await.unwrap().json::<ApiResponse<Candle>>().await.unwrap();
    if resp.code != "0" {
        return vec![];
    }
    resp.data
}

fn calc_change(candles: &[Candle]) -> f64 {
    if candles.len() < 2 {
        return 0.0;
    }
    let mut sorted: Vec<_> = candles.iter().collect();
    sorted.sort_by_key(|c| c.0[0].parse::<i64>().unwrap_or(0));
    let open: f64 = sorted[0].0[1].parse().unwrap_or(0.0);
    let close: f64 = sorted[sorted.len() - 1].0[4].parse().unwrap_or(0.0);
    if open == 0.0 {
        return 0.0;
    }
    (close - open) / open * 100.0
}

#[tokio::main]
async fn main() {
    println!("========================================");
    println!("  ClueBot Demo - 波动率做空策略扫描器");
    println!("========================================\n");

    // 加载环境变量
    dotenvy::dotenv().ok();

    // 初始化邮件通知渠道
    let email_channel = init_email_channel().await;

    let client = Client::new();

    println!("开始获取交易对数据...");
    
    let spot_tickers = get_tickers(&client, "SPOT").await;
    println!("获取到 {} 个现货交易对", spot_tickers.len());
    
    let swap_tickers = get_tickers(&client, "SWAP").await;
    println!("获取到 {} 个永续合约交易对", swap_tickers.len());

    let spot_map: HashMap<String, String> = spot_tickers
        .iter()
        .filter_map(|t| {
            if t.inst_id.ends_with("-USDT") {
                let coin = t.inst_id.replace("-USDT", "");
                Some((coin, t.inst_id.clone()))
            } else {
                None
            }
        })
        .collect();

    let swap_map: HashMap<String, String> = swap_tickers
        .iter()
        .filter_map(|t| {
            if t.inst_id.ends_with("-USDT-SWAP") {
                let coin = t.inst_id.replace("-USDT-SWAP", "");
                Some((coin, t.inst_id.clone()))
            } else {
                None
            }
        })
        .collect();

    let common_coins: Vec<_> = spot_map.keys().filter(|k| swap_map.contains_key(*k)).collect();
    println!("找到 {} 个共有币种\n", common_coins.len());

    println!("如下是符合要求，准备发出预警的币种：");
    println!("{}", "-".repeat(70));

    let mut matched_count = 0;
    let mut alerts: Vec<String> = Vec::new();

    for coin in common_coins {
        println!("\n正在检查币种: {}", coin);
        let spot_id = &spot_map[coin];
        let swap_id = &swap_map[coin];

        let spot_candles = get_candles(&client, spot_id).await;
        let swap_candles = get_candles(&client, swap_id).await;
        
        println!("  现货 {} 获取到 {} 根K线", spot_id, spot_candles.len());
        println!("  永续 {} 获取到 {} 根K线", swap_id, swap_candles.len());
        
        // 打印原始K线数据
        if !spot_candles.is_empty() {
            println!("  现货K线数据: {:?}", spot_candles);
        }
        if !swap_candles.is_empty() {
            println!("  永续K线数据: {:?}", swap_candles);
        }

        let spot_change = calc_change(&spot_candles);
        let swap_change = calc_change(&swap_candles);
        
        println!("  现货涨跌幅: {:.2}%", spot_change);
        println!("  永续涨跌幅: {:.2}%", swap_change);

        if spot_change > 10.0 && swap_change > 10.0 {
            let alert_msg = format!(
                "符合条件! 币种: {} | 现货 {}: +{:.2}% | 永续 {}: +{:.2}%",
                coin, spot_id, spot_change, swap_id, swap_change
            );
            println!("  >>> {}", alert_msg);
            println!("{}", "-".repeat(70));
            
            alerts.push(alert_msg);
            matched_count += 1;
        }
    }
    
    println!("\n检查完成，共 {} 个币种符合条件", matched_count);

    // 发送邮件通知
    if let Some(channel) = email_channel {
        if matched_count > 0 {
            let email_body = format!(
                "ClueBot 策略扫描完成\n\n发现 {} 个符合条件的币种:\n\n{}\n\n---\nGenerated by ClueBot Demo",
                matched_count,
                alerts.join("\n")
            );
            
            println!("\n正在发送邮件通知...");
            match channel.send(&email_body).await {
                Ok(_) => println!("✓ 邮件通知发送成功"),
                Err(e) => eprintln!("✗ 邮件通知发送失败: {}", e),
            }
        } else {
            let email_body = "ClueBot 策略扫描完成\n\n未发现符合条件的币种。\n\n---\nGenerated by ClueBot Demo";
            
            println!("\n正在发送邮件通知...");
            match channel.send(email_body).await {
                Ok(_) => println!("✓ 邮件通知发送成功"),
                Err(e) => eprintln!("✗ 邮件通知发送失败: {}", e),
            }
        }
    }

    println!("\n========================================");
    println!("  扫描完成");
    println!("========================================");
}

async fn init_email_channel() -> Option<EmailChannel> {
    // 读取邮箱配置
    let smtp_server = env::var("SMTP_SERVER").ok()?;
    let smtp_port = env::var("SMTP_PORT")
        .unwrap_or_else(|_| "587".to_string()).parse::<u16>().ok()?;
    let from_email = env::var("FROM_EMAIL").ok()?;
    let smtp_username = env::var("SMTP_USERNAME")
        .unwrap_or_else(|_| from_email.clone());
    let smtp_password = env::var("SMTP_PASSWORD").ok()?;
    let smtp_recipient = env::var("SMTP_RECIPIENT")
        .unwrap_or_else(|_| smtp_username.clone());
    let use_tls = env::var("USE_TLS")
        .unwrap_or_else(|_| "true".to_string()).parse::<bool>().ok()?;

    println!("邮件配置:");
    println!("  SMTP服务器: {}", smtp_server);
    println!("  发件人: {}", from_email);
    println!("  收件人: {}\n", smtp_recipient);

    // 创建邮箱配置
    let email_config = EmailConfig::custom(
        smtp_server,
        smtp_port,
        from_email,
        smtp_username,
        smtp_password,
        use_tls,
    );

    // 创建邮箱渠道并连接
    let mut email_channel = EmailChannel::new(email_config)
        .with_recipients(vec![smtp_recipient]);
    
    match email_channel.connect().await {
        Ok(_) => {
            println!("✓ SMTP连接成功\n");
            Some(email_channel)
        }
        Err(e) => {
            eprintln!("✗ SMTP连接失败: {}，将继续执行但不发送邮件\n", e);
            None
        }
    }
}
